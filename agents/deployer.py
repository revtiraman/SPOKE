    def _generate_tests(self, blueprint: Blueprint, code: str) -> str:
        name = blueprint.agent_name
        return textwrap.dedent(f"""
            \"\"\"Auto-generated tests for {name} — generated by Spoke.\"\"\"

            import asyncio
            import pytest
            import sys
            from pathlib import Path

            sys.path.insert(0, str(Path(__file__).parent.parent))


            def test_agent_module_imports():
                \"\"\"Agent module must import without errors.\"\"\"
                import importlib.util
                spec = importlib.util.spec_from_file_location("agent", Path(__file__).parent.parent / "agent.py")
                module = importlib.util.module_from_spec(spec)
                spec.loader.exec_module(module)
                assert hasattr(module, "demo")
                assert hasattr(module, "main")


            @pytest.mark.asyncio
            async def test_demo_runs_successfully():
                \"\"\"Demo mode must complete without raising exceptions.\"\"\"
                import subprocess, sys
                result = subprocess.run(
                    [sys.executable, "agent.py", "--mode", "demo"],
                    capture_output=True, text=True, timeout=60,
                    cwd=Path(__file__).parent.parent
                )
                assert result.returncode == 0, f"Demo failed:\\n{{result.stderr}}"


            def test_env_example_exists():
                assert (Path(__file__).parent.parent / ".env.example").exists()


            def test_requirements_complete():
                req = (Path(__file__).parent.parent / "requirements.txt").read_text()
                for pkg in ["loguru", "tenacity", "rich", "pydantic"]:
                    assert pkg in req, f"{{pkg}} missing from requirements.txt"
        """).strip()

    def _generate_monitoring(self, blueprint: Blueprint) -> str:
        name = blueprint.agent_name
        return textwrap.dedent(f"""
            \"\"\"
            {name} — Self-monitoring module.
            Run alongside the agent to track health and send alerts.
            Generated by Spoke.
            \"\"\"

            import os
            import sqlite3
            import time
            from datetime import datetime
            from pathlib import Path

            from loguru import logger

            CACHE_DB = os.getenv("CACHE_DB", "storage/{name.lower()}_cache.db")
            ALERT_EMAIL = os.getenv("ALERT_EMAIL", "")
            CHECK_INTERVAL = int(os.getenv("MONITOR_CHECK_INTERVAL", "300"))  # 5 min


            def get_stats() -> dict:
                \"\"\"Pull current stats from the dedup cache.\"\"\"
                try:
                    with sqlite3.connect(CACHE_DB) as conn:
                        total = conn.execute("SELECT COUNT(*) FROM processed_emails").fetchone()[0]
                        recent = conn.execute(
                            "SELECT COUNT(*) FROM processed_emails WHERE processed_at > datetime('now', '-1 hour')"
                        ).fetchone()[0]
                        return {{"total_processed": total, "last_hour": recent, "healthy": True}}
                except Exception as e:
                    return {{"error": str(e), "healthy": False}}


            def check_health() -> bool:
                \"\"\"Return True if agent is running and processing orders.\"\"\"
                stats = get_stats()
                if not stats.get("healthy"):
                    logger.error(f"Health check failed: {{stats.get('error')}}")
                    return False
                logger.info(f"Health: {{stats['total_processed']}} total, {{stats['last_hour']}} in last hour")
                return True


            if __name__ == "__main__":
                logger.info(f"{name} Monitor starting (check every {{CHECK_INTERVAL}}s)")
                while True:
                    check_health()
                    time.sleep(CHECK_INTERVAL)
        """).strip()

