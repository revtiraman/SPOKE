"""Agent 8 — Deployer: packages working code, generates report, and creates downloadable artifact."""

from __future__ import annotations
import os
import textwrap
import zipfile
from datetime import datetime
from pathlib import Path

from loguru import logger

from core.config import settings
from core.models import Blueprint, DeploymentPackage, ProblemGraph
from llm.client import get_client
from llm.router import router


class DeployerAgent:
    """Packages and deploys a working agent, generates the final report."""

    async def deploy(
        self,
        code: str,
        execution_output: str,
        problem: ProblemGraph,
        blueprint: Blueprint,
        session_id: str = "DEMO",
    ) -> DeploymentPackage:
        """Create a complete deployment package for the working agent."""
        logger.info(f"Deploying {blueprint.agent_name}...")

        requirements = self._generate_requirements(blueprint)
        env_example = self._generate_env_example(blueprint)
        setup_script = self._generate_setup_script(blueprint)
        readme = self._generate_readme(blueprint, problem, execution_output)
        report = await self._generate_report(blueprint, problem, execution_output)

        # Write files to disk
        output_dir = settings.generated_dir / session_id
        output_dir.mkdir(parents=True, exist_ok=True)

        dockerfile = self._generate_dockerfile(blueprint)
        docker_compose = self._generate_docker_compose(blueprint)
        tests_code = self._generate_tests(blueprint, code)
        monitoring = self._generate_monitoring(blueprint)
        github_ci = self._generate_github_ci(blueprint)

        files = {
            "agent.py": code,
            "requirements.txt": requirements,
            ".env.example": env_example,
            "setup.sh": setup_script,
            "README.md": readme,
            "REPORT.md": report,
            "Dockerfile": dockerfile,
            "docker-compose.yml": docker_compose,
            "tests/test_agent.py": tests_code,
            "monitoring.py": monitoring,
            ".github/workflows/ci.yml": github_ci,
        }

        for filename, content in files.items():
            filepath = output_dir / filename
            filepath.parent.mkdir(parents=True, exist_ok=True)
            filepath.write_text(content, encoding="utf-8")

        zip_path = self._create_zip(output_dir, blueprint.agent_name, files)

        logger.success(f"Deployment package created: {zip_path}")

        return DeploymentPackage(
            agent_name=blueprint.agent_name,
            agent_code=code,
            requirements=requirements,
            readme=readme,
            setup_script=setup_script,
            env_example=env_example,
            report=report,
            output_dir=str(output_dir),
            zip_path=zip_path,
        )

    def _generate_requirements(self, blueprint: Blueprint) -> str:
        base = textwrap.dedent("""
            # Auto-generated by Spoke
            httpx==0.26.0
            loguru==0.7.2
            tenacity==8.2.3
            rich==13.7.1
            pydantic==2.6.3
            python-dotenv==1.0.1
            aiofiles==23.2.1
        """).strip()

        extras = []
        for tool in blueprint.tools_required:
            name_lower = tool.name.lower()
            if "gmail" in name_lower or "google" in name_lower:
                extras += ["google-api-python-client==2.120.0", "google-auth-httplib2==0.2.0", "google-auth-oauthlib==1.2.0"]
            elif "slack" in name_lower:
                extras.append("slack-sdk==3.27.0")
            elif "notion" in name_lower:
                extras.append("notion-client==2.2.1")
            elif "hubspot" in name_lower or "crm" in name_lower:
                extras.append("hubspot-api-client==8.2.1")
            elif "openai" in name_lower:
                extras.append("openai==1.13.3")
            elif "anthropic" in name_lower:
                extras.append("anthropic==0.21.3")

        seen = set()
        unique_extras = [x for x in extras if not (x in seen or seen.add(x))]
        if unique_extras:
            base += "\n\n# Integration-specific\n" + "\n".join(unique_extras)
        return base

    def _generate_env_example(self, blueprint: Blueprint) -> str:
        lines = ["# Environment variables for " + blueprint.agent_name, "# Copy to .env and fill in your values", ""]
        for tool in blueprint.tools_required:
            name_upper = tool.name.upper().replace(" ", "_").replace("-", "_")
            auth = tool.auth.lower()
            if "oauth" in auth:
                lines.append(f"GOOGLE_SERVICE_ACCOUNT_FILE=credentials.json  # {tool.name} OAuth2")
            elif "api key" in auth:
                lines.append(f"{name_upper}_API_KEY=your_{name_upper.lower()}_key_here")
            else:
                lines.append(f"{name_upper}_TOKEN=your_token_here")

        lines += [
            "",
            "# HuggingFace (for LLM extraction)",
            "HF_API_TOKEN=hf_your_token_here",
            "",
            "# Agent settings",
            "POLL_INTERVAL_SECONDS=60",
            "LOG_FILE=logs/agent.log",
        ]
        return "\n".join(lines)

