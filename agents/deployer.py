    def _generate_dockerfile(self, blueprint: Blueprint) -> str:
        name_lower = blueprint.agent_name.lower().replace(" ", "_")
        return textwrap.dedent(f"""
            FROM python:3.11-slim

            LABEL org.opencontainers.image.title="{blueprint.agent_name}"
            LABEL org.opencontainers.image.description="{blueprint.agent_description}"
            LABEL org.opencontainers.image.source="Built by Spoke"

            WORKDIR /app

            RUN apt-get update && apt-get install -y --no-install-recommends \\
                gcc curl \\
                && rm -rf /var/lib/apt/lists/*

            COPY requirements.txt .
            RUN pip install --no-cache-dir -r requirements.txt

            COPY . .

            RUN mkdir -p logs storage

            ENV PYTHONUNBUFFERED=1
            ENV PYTHONPATH=/app

            HEALTHCHECK --interval=60s --timeout=10s --start-period=5s \\
                CMD python -c "import sqlite3; sqlite3.connect('storage/{name_lower}_cache.db')" || exit 1

            CMD ["python", "agent.py", "--mode", "production"]
        """).strip()

